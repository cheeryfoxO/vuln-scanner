"""Tests for technology fingerprint module."""
from unittest.mock import Mock
from scanner.modules.fingerprint import (
    _check_header_techs,
    _check_html_techs,
    _check_cookie_techs,
    _detect_waf,
    FingerprintModule,
)


class TestHeaderTechs:
    def test_detects_nginx(self):
        techs = _check_header_techs({"Server": "nginx/1.25.0"})
        assert "nginx" in techs

    def test_detects_apache(self):
        techs = _check_header_techs({"Server": "Apache/2.4.57 (Ubuntu)"})
        assert "Apache" in techs

    def test_detects_php(self):
        techs = _check_header_techs({"X-Powered-By": "PHP/8.1"})
        assert "PHP" in techs

    def test_detects_cloudflare(self):
        techs = _check_header_techs({"Server": "cloudflare"})
        assert "Cloudflare" in techs

    def test_unknown_header_returns_empty(self):
        techs = _check_header_techs({"Server": "UnknownProxy/1.0"})
        assert len(techs) == 0


