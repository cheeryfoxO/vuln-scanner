class TestDetectWaf:
    def test_detects_cloudflare(self):
        headers = {"Server": "cloudflare", "CF-Ray": "abc123"}
        html = "<html>Checking your browser</html>"
        wafs = _detect_waf(headers, html)
        assert any(w["waf"] == "Cloudflare" for w in wafs)

    def test_detects_modsecurity(self):
        headers = {}
        html = "This error was generated by Mod_Security"
        wafs = _detect_waf(headers, html)
        assert any(w["waf"] == "ModSecurity" for w in wafs)

    def test_no_waf_returns_empty(self):
        headers = {"Server": "nginx"}
        html = "<html><body>Welcome</body></html>"
        wafs = _detect_waf(headers, html)
        assert len(wafs) == 0

    def test_cloudflare_waf_has_bypass_tips(self):
        headers = {"Server": "cloudflare", "CF-Ray": "abc"}
        html = "Checking your browser"
        wafs = _detect_waf(headers, html)
        cf = [w for w in wafs if w["waf"] == "Cloudflare"][0]
        assert len(cf["bypass_tips"]) > 0
        assert cf["confidence"] == "high"


class TestFingerprintModule:
    def test_module_attributes(self):
        m = FingerprintModule()
        assert m.name == "fingerprint"
        assert m.requires_url is True
        assert "fingerprint" in m.description.lower()

    def test_recommends_modules_for_php(self):
        rec = FingerprintModule._recommend_modules(
            {"PHP", "PHP Session", "nginx"}, [], None
        )
        assert "sqli" in rec
        assert "lfi" in rec
        assert "headers" in rec

    def test_waf_discards_reflected_xss(self):
        rec = FingerprintModule._recommend_modules(
            {"PHP"},
            [{"waf": "Cloudflare", "confidence": "high", "bypass_tips": []}],
            None
        )
        assert "xss" not in rec
        assert "dom_xss" in rec
